        sysOptions.setExportPDF(jPDFCheckBox.isSelected());
        sysOptions.setExportPNG(jPNGCheckBox.isSelected());
        sysOptions.setExportGIF(jGIFCheckBox.isSelected());
        sysOptions.setExportJPEG(jJPEGCheckBox.isSelected());
